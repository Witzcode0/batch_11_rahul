// Types of Operators in C

// 1. Arithmetic Operators (Math Operations)

// | Operator | Meaning             | Example |
// | -------- | ------------------- | ------- |
// | `+`      | Addition            | `a + b` |
// | `-`      | Subtraction         | `a - b` |
// | `*`      | Multiplication      | `a * b` |
// | `/`      | Division            | `a / b` |
// | `%`      | Modulus (remainder) | `a % b` |


// 2. Relational Operators (Comparison)
// | Operator | Meaning               |
// | -------- | --------------------- |
// | `==`     | Equal to              |
// | `!=`     | Not equal to          |
// | `>`      | Greater than          |
// | `<`      | Less than             |
// | `>=`     | Greater than or equal |
// | `<=`     | Less than or equal    |


// ðŸ‘‰ Output is always True (1) or False (0).

