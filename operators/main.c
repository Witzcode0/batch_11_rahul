// ðŸ”¹ 4. Assignment/Shorthand Operators (Assign Values)

// | Operator | Example  | Meaning      |
// | -------- | -------- | ------------ |
// | `=`      | `a = 10` | Assign value |
// | `+=`     | `a += 5` | a = a + 5    |
// | `-=`     | `a -= 5` | a = a - 5    |
// | `*=`     | `a *= 5` | a = a * 5    |
// | `/=`     | `a /= 5` | a = a / 5    |
// | `%=`     | `a %= 5` | a = a % 5    |

