// ðŸ”¹ 5. Increment / Decrement Operators
// | Operator | Meaning             |
// | -------- | ------------------- |
// | `++`     | Increase value by 1 |
// | `--`     | Decrease value by 1 |


// Example:

// int a = 5;
// a++; // now a = 6

