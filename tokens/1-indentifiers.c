// Identifiers in C are the names used to identify variables, functions, arrays, structures, or any other user-defined items in a program. They help the compiler and programmers recognize different elements of the code.

// Must begin with a letter (A–Z or a–z) or an underscore (_).
// ✔️ name, _total
// ❌ 1name

// Cannot start with a digit.
// ✔️ marks1
// ❌ 9marks

// Cannot be a C keyword (reserved word).
// ❌ int, float, return, if

// Can contain only letters, digits, and underscores.
// ✔️ student_1, totalMarks
// ❌ total-marks, price@

// No spaces are allowed.
// ✔️ userName
// ❌ user name

// Identifiers are case-sensitive.
// Total, total, and TOTAL are all different.

// Should not use special characters like @, #, %, &, *, etc.

// Must be unique within their scope (you cannot declare two variables with the same name in the same block).